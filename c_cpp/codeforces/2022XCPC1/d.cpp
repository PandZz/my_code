#include <cmath>
#include <cstdio>
#include <cstdlib>
#include <cstring>
#include <iostream>
using namespace std;
typedef long long LL;
LL n, q, u, len;
char a[100005];
int main()
{
    scanf("%lld%lld", &n, &q);
    for (int i = 1; i <= q; ++i)
    {
        scanf("%d %s", &u, a + 1);
        len = strlen(a + 1);
    }
    return 0;
}